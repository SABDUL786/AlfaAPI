package com.alfamodels;

public class CreditInformation {
	private float boeWeight;
	private boolean lineOfCreditRequired;

	// Getter Methods

	public float getBoeWeight() {
		return boeWeight;
	}

	public boolean getLineOfCreditRequired() {
		return lineOfCreditRequired;
	}

	// Setter Methods

	public void setBoeWeight(float boeWeight) {
		this.boeWeight = boeWeight;
	}

	public void setLineOfCreditRequired(boolean lineOfCreditRequired) {
		this.lineOfCreditRequired = lineOfCreditRequired;
	}
}
